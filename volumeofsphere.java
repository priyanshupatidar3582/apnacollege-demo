package java_basics;
public class volumeofsphere {
    public static void main(String[] args) {
        double r = 7;
        double pi = 3.1415;
        double volume = 4.0/3.0 * pi * r * r * r;
        System.out.println("The volume of sphere is : "+volume);
    }
}
//The volume of sphere is : 1436.7126666666666