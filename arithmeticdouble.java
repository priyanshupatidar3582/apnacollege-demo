package java_basics;
public class arithmeticdouble {
    public static void main(String[] args) {
        double x = 5;
        double y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        
        // 7.0
        // 3.0
        // 10.0
        // 2.5

    }
}
