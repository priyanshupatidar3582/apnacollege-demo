//example 1 :- calculate percentage of 5 subject where maximum marks of one sublect is 100
package java_basics;
public class percentageof5subject {
    public static void main(String[] args) {
        double m1 = 92; //english marks
        double m2 = 84; //hindi marks
        double m3 = 60; //maths marks
        double m4 = 65; //physics marks
        double m5 = 72; //chemistry marks
        double percentage = (m1 + m2 + m3 + m4 + m5)/5;
        System.out.println(percentage);
    }
}

//74.6
